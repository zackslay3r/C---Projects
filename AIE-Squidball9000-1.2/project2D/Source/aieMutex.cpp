#include "aieMutex.h"


/******************************************************************************
*******************************************************************************/
aie::Mutex::Mutex()
{
    InitializeCriticalSection(&m_cs);
}

/******************************************************************************
*******************************************************************************/
aie::Mutex::~Mutex()
{
    try
    {
        DeleteCriticalSection(&m_cs);
    }
    catch (...)
    {
    }
}

/*****************************************************************************
******************************************************************************/
void aie::Mutex::lock()
{
    EnterCriticalSection(&m_cs);
}

/*****************************************************************************
******************************************************************************/
void aie::Mutex::unlock()
{
    LeaveCriticalSection(&m_cs);
}

